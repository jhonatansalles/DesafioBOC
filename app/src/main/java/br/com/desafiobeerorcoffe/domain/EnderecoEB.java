package br.com.desafiobeerorcoffe.domain;

/**
 * @author Jhonatan
 */
public class EnderecoEB {
    private String logradouro;
    private String numero;
    private String bairro;
    private String cidade;
    private String uf;
    private String pais;
    private String latitude;
    private String longitude;
    private boolean obterEnderecoCompleto;

    public boolean isObterEnderecoCompleto() {
        return obterEnderecoCompleto;
    }

    public void setObterEnderecoCompleto(boolean obterEnderecoCompleto) {
        this.obterEnderecoCompleto = obterEnderecoCompleto;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    private String mensagemErro;

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getMensagemErro() {
        return mensagemErro;
    }

    public void setMensagemErro(String mensagemErro) {
        this.mensagemErro = mensagemErro;
    }
}
